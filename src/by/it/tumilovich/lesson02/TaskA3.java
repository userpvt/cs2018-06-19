/*
Измените переменные и строку вывода в программе так,
чтобы она рассчитывала и печатала следующее выражение
3*3+4*4=25
 */

class TaskA3 {
    public static void main(String[] args) {
        int i = 4;
        int j = 3;
        int k = 25;
        System.out.println(j + "*" + j + "+" + i + "*" + i + "=" + k);
    }
}
